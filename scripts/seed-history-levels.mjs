function level(spec) {
  const steps = spec.steps.map((s, i) => ({
    id: `s${i + 1}`,
    kind: "branch",
    narration: s.narration,
    prompt: s.prompt,
    options: [
      {
        id: "a",
        label: s.a.label,
        isTarget: true,
        outcome: s.a.outcome,
      },
      {
        id: "b",
        label: s.b.label,
        outcome: s.b.outcome,
      },
    ],
    ...(s.whatIf
      ? {
          whatIf: {
            teaser: s.whatIf.teaser,
            alternateText: s.whatIf.alt,
            revealText: s.whatIf.reveal,
          },
        }
      : {}),
  }));

  return {
    id: spec.id,
    type: "history",
    title: spec.title,
    subtitle: spec.subtitle,
    era: spec.era,
    author: spec.author,
    protagonist: spec.protagonist,
    difficulty: spec.difficulty ?? 1,
    intro: spec.intro,
    guide: spec.guide ?? "你将替文中人物做几个选择。选对更接近课文与史实。",
    steps,
    scoring: {
      mode: "fidelity",
      bands: [
        { min: 0, max: 1, text: spec.bandLow ?? "你的选择与课文略有出入，但故事仍说得通。" },
        { min: 2, max: 2, text: spec.bandHigh ?? "你与课文、史意相合。" },
      ],
    },
    reveal: {
      realText: spec.realText,
      source: spec.source,
      analysis: spec.analysis,
      takeaway: spec.takeaway,
    },
    shareCard: {
      headline: `${spec.title} · 你走过了这一程`,
      footer: spec.takeaway,
    },
    tags: spec.tags ?? ["课标", spec.era],
    facets: spec.facets,
  };
}

